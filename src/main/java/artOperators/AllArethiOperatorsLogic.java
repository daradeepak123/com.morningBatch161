package artOperators;

public class AllArethiOperatorsLogic {
	
	
	int a;
	int b;
	
	
	public int addNum(int a,int b)
	{
		this.a=a;
		this.b=b;
		return this.a+this.b;		
		
	}
	
	
	public int addNum(int a)
	{
		this.a=a;
		
		return this.a+100;		
		
	}
	
	
	
	public int subNum(int a,int b)
	{
		this.a=a;
		this.b=b;
		return this.a-this.b;		
		
	}
	
	
	

}
